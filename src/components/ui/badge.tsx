import * as React from "react"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const badgeVariants = cva(
  "inline-flex items-center border px-2.5 py-0.5 font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
  {
    variants: {
      variant: {
        default:
          "border-transparent bg-primary text-primary-foreground hover:bg-primary/80",
        secondary:
          "border-transparent bg-secondary text-secondary-foreground hover:bg-secondary/80",
        destructive:
          "border-transparent bg-destructive text-destructive-foreground hover:bg-destructive/80",
        outline: "text-foreground",
        // 追加バリアント
        success: "border-transparent bg-green-100 text-green-700 hover:bg-green-200",
        warning: "border-transparent bg-yellow-100 text-yellow-800 hover:bg-yellow-200",
        info: "border-transparent bg-blue-100 text-blue-700 hover:bg-blue-200",
        purple: "border-transparent bg-purple-100 text-purple-700 hover:bg-purple-200",
        gray: "border-transparent bg-gray-100 text-gray-700 hover:bg-gray-200",
        cancelled: "border-transparent bg-red-100 text-red-700 line-through opacity-70",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
)

const sizeMap = {
  sm: "text-[10px] px-1.5 py-0 h-5",
  md: "text-xs px-2.5 py-0.5",
  lg: "text-sm px-3 py-1"
}

export interface BadgeProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof badgeVariants> {
  size?: "sm" | "md" | "lg"
}

function Badge({ className, variant, size = "md", ...props }: BadgeProps) {
  return (
    <div
      className={cn(
        badgeVariants({ variant }),
        sizeMap[size],
        className
      )}
      {...props}
    />
  )
}

export { Badge, badgeVariants }
