    gms = re.split(r'[,ã€/]', gm_text)
    
    # ã‚¯ãƒªãƒ¼ãƒ³ã‚¢ãƒƒãƒ—
    gms = [gm.strip() for gm in gms if gm.strip()]
    
    return gms

