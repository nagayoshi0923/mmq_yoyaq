-- =============================================================================
-- scenario_import_aliases
-- スケジュールインポート時のシナリオ名エイリアス（略称→正式名称）をDB管理
-- これまで ImportScheduleModal.tsx にハードコードしていたマッピングを移行
-- =============================================================================

CREATE TABLE public.scenario_import_aliases (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  alias TEXT NOT NULL,
  canonical_name TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  CONSTRAINT scenario_import_aliases_alias_key UNIQUE (alias)
);

ALTER TABLE public.scenario_import_aliases ENABLE ROW LEVEL SECURITY;

-- authenticated ユーザー（スタッフ・管理者）は参照可能
CREATE POLICY "authenticated_can_read_scenario_import_aliases"
  ON public.scenario_import_aliases FOR SELECT TO authenticated USING (true);

-- 管理者のみ追加・編集・削除可能
CREATE POLICY "admin_can_manage_scenario_import_aliases"
  ON public.scenario_import_aliases FOR ALL TO authenticated
  USING (is_org_admin())
  WITH CHECK (is_org_admin());

COMMENT ON TABLE public.scenario_import_aliases IS
'スケジュールインポート時のシナリオ名エイリアス。略称・表記ゆれを正式名称に変換するために使用。';

